#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { realpathSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

export const GENERATED_BRANCH_RE = /^stack\/[^/]+\/.+--[0-9a-f]{6,}$/;

export function isGeneratedStackBranch(name) {
  return typeof name === 'string' && GENERATED_BRANCH_RE.test(name);
}

export function evaluatePush({ branch, mergifyRefusesAsGenerated }) {
  if (isGeneratedStackBranch(branch)) {
    return { allowed: false, reason: `branch name "${branch}" is a Mergify-generated stack branch` };
  }
  if (mergifyRefusesAsGenerated) {
    return { allowed: false, reason: `mergify reports "${branch}" is a generated stack branch` };
  }
  return { allowed: true, reason: 'working branch' };
}

function git(args) {
  return execFileSync('git', args, { encoding: 'utf8' }).trim();
}

function currentBranch() {
  return git(['rev-parse', '--abbrev-ref', 'HEAD']);
}

function mergifyRefusesAsGenerated() {
  try {
    execFileSync('mergify', ['stack', 'list'], { encoding: 'utf8', stdio: 'pipe' });
    return false;
  } catch (error) {
    const out = `${error.stdout || ''}${error.stderr || ''}`;
    return /generated by Mergify CLI/i.test(out);
  }
}

function refuse(branch, reason) {
  console.error(
    [
      `safe-stack-push: REFUSING to push — ${reason}.`,
      '',
      'Never run `mergify stack push` from a generated stack/ branch: the real',
      'push does not fail safe and can publish an unrelated stack (incident',
      'PR #3407 -> accidental #3408–#3411).',
      '',
      'Recovery:',
      '  • To re-push your stack: switch to the WORKING branch (e.g. `pr/<name>`)',
      '    and re-run this guard from there.',
      '  • To update ONE already-published branch: `git push --force-with-lease origin HEAD`.',
      '',
      `Current branch: ${branch}`,
    ].join('\n'),
  );
  process.exit(1);
}

function main() {
  const argv = process.argv.slice(2);
  const execute = argv.includes('--execute');
  const passthrough = argv.filter((a) => a !== '--execute');

  const branch = currentBranch();
  const decision = evaluatePush({ branch, mergifyRefusesAsGenerated: mergifyRefusesAsGenerated() });
  if (!decision.allowed) refuse(branch, decision.reason);

  console.error(`safe-stack-push: previewing plan on "${branch}" (dry-run)…`);
  let plan;
  try {
    plan = execFileSync('mergify', ['stack', 'push', '--dry-run', ...passthrough], { encoding: 'utf8', stdio: 'pipe' });
  } catch (error) {
    console.error(`safe-stack-push: dry-run failed — not pushing.\n${error.stdout || ''}${error.stderr || ''}`);
    process.exit(1);
  }
  process.stderr.write(plan.endsWith('\n') ? plan : `${plan}\n`);

  if (!execute) {
    console.error('\nDry-run only. Confirm the plan above pushes ONLY your intended change, then re-run with --execute.');
    return;
  }

  console.error('\nsafe-stack-push: executing real push…');
  execFileSync('mergify', ['stack', 'push', ...passthrough], { stdio: 'inherit' });
}

const invokedDirectly = (() => {
  try {
    return realpathSync(process.argv[1]) === realpathSync(fileURLToPath(import.meta.url));
  } catch {
    return false;
  }
})();

if (invokedDirectly) main();
