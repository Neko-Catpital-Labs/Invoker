#!/usr/bin/env node
import { execFileSync } from 'node:child_process';
import path from 'node:path';
import process from 'node:process';

const CHECKED_EXTENSIONS = new Set(['.cjs', '.js', '.jsx', '.mjs', '.sh', '.ts', '.tsx']);
const SKIPPED_PATH_PARTS = new Set(['__generated__', '__tests__', 'coverage', 'dist', 'docs', 'e2e', 'fixtures', 'node_modules', 'out', 'repro', 'tests']);
const ALLOWED_COMMENT_PATTERN = /(?:@ts-expect-error|@ts-ignore|eslint|shellcheck|istanbul|c8|prettier-ignore|generated|autogenerated|auto-generated|copyright|license|spdx)/i;

function usage() {
  console.error('Usage: node scripts/check-added-comments.mjs [--base <ref>] [--root <path>]');
}

function parseArgs(argv) {
  const parsed = {
    base: process.env.INVOKER_COMMENT_DIFF_BASE || '',
    root: process.cwd(),
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--base') {
      parsed.base = argv[index + 1] || '';
      index += 1;
    } else if (arg.startsWith('--base=')) {
      parsed.base = arg.slice('--base='.length);
    } else if (arg === '--root') {
      parsed.root = argv[index + 1] || '';
      index += 1;
    } else if (arg.startsWith('--root=')) {
      parsed.root = arg.slice('--root='.length);
    } else if (arg === '--help' || arg === '-h') {
      usage();
      process.exit(0);
    } else {
      console.error(`[comments] Unknown argument: ${arg}`);
      usage();
      process.exit(2);
    }
  }

  if (!parsed.root) {
    usage();
    process.exit(2);
  }

  return {
    base: parsed.base,
    root: path.resolve(parsed.root),
  };
}

function runGit(root, args) {
  return execFileSync('git', args, {
    cwd: root,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
  });
}

function hasGitRef(root, ref) {
  try {
    runGit(root, ['rev-parse', '--verify', `${ref}^{commit}`]);
    return true;
  } catch {
    return false;
  }
}

function defaultBase(root) {
  const candidates = [];
  if (process.env.GITHUB_BASE_REF) {
    candidates.push(`origin/${process.env.GITHUB_BASE_REF}`);
  }
  candidates.push('origin/master', 'origin/main');

  for (const candidate of candidates) {
    if (hasGitRef(root, candidate)) {
      return candidate;
    }
  }

  if (process.env.CI) {
    console.error('[comments] Could not find a base ref. Set INVOKER_COMMENT_DIFF_BASE.');
    process.exit(2);
  }

  return '';
}

function isCheckedFile(filePath) {
  const normalized = filePath.split(path.sep).join('/');
  const basename = path.basename(normalized);
  const extension = path.extname(basename);

  if (!CHECKED_EXTENSIONS.has(extension)) {
    return false;
  }
  if (basename.endsWith('.d.ts') || basename.includes('.generated.') || basename.includes('.gen.')) {
    return false;
  }
  if (basename.includes('.test.') || basename.includes('.spec.') || basename.includes('.stories.') || basename.startsWith('test-')) {
    return false;
  }

  const parts = normalized.split('/');
  return !parts.some((part) => SKIPPED_PATH_PARTS.has(part));
}

function commentIndexForCodeLine(line) {
  let quote = '';
  let escaped = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1] || '';

    if (quote) {
      if (escaped) {
        escaped = false;
      } else if (char === '\\') {
        escaped = true;
      } else if (char === quote) {
        quote = '';
      }
      continue;
    }

    if (char === '"' || char === "'" || char === '`') {
      quote = char;
      continue;
    }
    if (char === '/' && (next === '/' || next === '*')) {
      return index;
    }
    if (char === '*' && next === '/') {
      return index;
    }
  }

  return -1;
}

function commentIndexForShellLine(line) {
  if (line.startsWith('#!')) {
    return -1;
  }

  let quote = '';
  let escaped = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];

    if (quote) {
      if (escaped) {
        escaped = false;
      } else if (char === '\\' && quote !== "'") {
        escaped = true;
      } else if (char === quote) {
        quote = '';
      }
      continue;
    }

    if (char === '"' || char === "'") {
      quote = char;
      continue;
    }
    if (char === '#') {
      return index;
    }
  }

  return -1;
}

function addedCommentIndex(filePath, line) {
  const extension = path.extname(filePath);
  if (extension === '.sh') {
    return commentIndexForShellLine(line);
  }
  return commentIndexForCodeLine(line);
}

function isAllowedComment(line, index) {
  return ALLOWED_COMMENT_PATTERN.test(line.slice(index));
}

export function collectAddedCommentViolations(diffText, source = 'diff') {
  const violations = [];
  const lines = diffText.split('\n');
  let currentPath = '';
  let checked = false;
  let newLine = 0;

  for (const line of lines) {
    if (line.startsWith('+++ ')) {
      const markerPath = line.slice(4).trim();
      currentPath = markerPath.startsWith('b/') ? markerPath.slice(2) : markerPath;
      checked = currentPath !== '/dev/null' && isCheckedFile(currentPath);
      continue;
    }

    if (line.startsWith('@@ ')) {
      const match = /\+(\d+)(?:,(\d+))?/.exec(line);
      newLine = match ? Number.parseInt(match[1], 10) : 0;
      continue;
    }

    if (!currentPath || newLine < 1) {
      continue;
    }

    if (line.startsWith('+') && !line.startsWith('+++')) {
      const addedLine = line.slice(1);
      if (checked) {
        const index = addedCommentIndex(currentPath, addedLine);
        if (index >= 0 && !isAllowedComment(addedLine, index)) {
          violations.push({
            source,
            path: currentPath,
            line: newLine,
            text: addedLine.trim(),
          });
        }
      }
      newLine += 1;
      continue;
    }

    if (line.startsWith('-') && !line.startsWith('---')) {
      continue;
    }

    if (line.startsWith(' ') || line === '') {
      newLine += 1;
    }
  }

  return violations;
}

function diffSources(root, base) {
  const sources = [];
  if (base) {
    sources.push({
      name: `${base}...HEAD`,
      text: runGit(root, ['diff', '--unified=0', '--diff-filter=ACMRT', `${base}...HEAD`, '--']),
    });
  }

  sources.push({
    name: 'staged changes',
    text: runGit(root, ['diff', '--cached', '--unified=0', '--diff-filter=ACMRT', '--']),
  });
  sources.push({
    name: 'working tree changes',
    text: runGit(root, ['diff', '--unified=0', '--diff-filter=ACMRT', '--']),
  });

  return sources;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const base = args.base || defaultBase(args.root);
  const violations = diffSources(args.root, base).flatMap((source) => collectAddedCommentViolations(source.text, source.name));

  if (violations.length > 0) {
    console.error(`[comments] Found ${violations.length} newly-added comment line(s) in checked source files.`);
    console.error('[comments] Prefer clearer names, smaller functions, or simpler control flow.');
    console.error('[comments] Allowed comments: license, generated markers, tool directives, and rare safety invariants.');
    for (const violation of violations) {
      console.error(`[comments] ${violation.path}:${violation.line} (${violation.source}) ${violation.text}`);
    }
    process.exit(1);
  }

  console.log('[comments] Checked added source lines; no disallowed comments found.');
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
